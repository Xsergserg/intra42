int		ft_mul(int x, int y)
{
	return (x * y);
}

int		ft_sum(int x, int y)
{
	return (x + y);
}

int		ft_dec(int x, int y)
{
	return (x - y);
}

int		ft_mod(int x, int y)
{
	return (x % y);
}

int		ft_div(int x, int y)
{
	return (x / y);
}
